/*Сдать решение задачи F3-Цифры по возрастанию

Цифры по возрастанию
Написать функцию и программу демонстрирующую работу данной функции. Вывести в порядке возрастания цифры, 
входящие в десятичную запись натурального числа N, не более 1000 цифр. Цифра пробел сколько раз данная цифра 
встречается в числе.
Формат входных данных
   Натуральное число не более 1000 цифр
Формат результата
   Цифры входящие в число через пробел в порядке возрастания. От самой младшей цифры до самой старшей и 
   количество.*/

#include <stdio.h>

// Функция для подсчета и вывода цифр в порядке возрастания
void print_digits_in_order(char *number)
{
   int digit_count[10] = {0}; // Массив для подсчета вхождений каждой цифры

   // Подсчет вхождений каждой цифры
   for (int i = 0; number[i] != '\0'; i++)
   {
      if (number[i] >= '0' && number[i] <= '9')
      {
         digit_count[number[i] - '0']++;
      }
   }

   // Вывод цифр в порядке возрастания
   for (int i = 0; i < 10; i++)
   {
      if (digit_count[i] > 0)
      {
         printf("%d %d\n", i, digit_count[i]);
      }
   }
}

// Пример использования функции
int main()
{
   char number[1001]; // Массив для хранения числа (до 1000 цифр + '\0')

   // Ввод числа
   scanf("%s", number);

   // Вызов функции для вывода цифр в порядке возрастания
   print_digits_in_order(number);

   return 0;
}
