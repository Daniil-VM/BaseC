/*Сдать решение задачи D3-В обратном порядке
В обратном порядке
Дано целое не отрицательное число N. Выведите все его цифры по одной, в обратном порядке, разделяя 
их пробелами или новыми строками. Составить рекурсивную функцию.
Формат входных данных
   Одно не отрицательное целое число
Формат результата
   Последовательность цифр введенного числа в обратном порядке через пробел */

#include <stdio.h>

// Рекурсивная функция для вывода цифр числа в обратном порядке
void printDigitsReverse(int n) 
{
   // Базовый случай: если число равно 0, ничего не делаем
    if (n == 0) 
   {
      return;
   }
    
   // Выводим последнюю цифру числа
   printf("%d ", n % 10);
    
   // Рекурсивный вызов для оставшейся части числа
   printDigitsReverse(n / 10);
}

int main() 
{
   int N;

   // Ввод числа
   scanf("%d", &N);

   // Если число равно 0, выводим его сразу
   if (N == 0)
   {
      printf("0\n");
   } else 
   {
   // Вызов рекурсивной функции для вывода цифр в обратном порядке
      printDigitsReverse(N);
      printf("\n");
   }
    
   return 0;
}